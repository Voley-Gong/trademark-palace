def main():
    data = json.loads(SRC.read_text(encoding="utf-8"))
    articles = data["articles"]
    missing = []
    for key, art in articles.items():
        exp = EXPLAINS.get(key)
        if not exp:
            missing.append(key)
            continue
        art["plain"] = exp["plain"]
        art["examples"] = exp["examples"]
        art["apply"] = exp["apply"]
    if missing:
        raise SystemExit(f"missing explains for: {missing}")
    data["schemaVersion"] = max(int(data.get("schemaVersion") or 3), 4)
    text = json.dumps(data, ensure_ascii=False, indent=2) + "\n"
    SRC.write_text(text, encoding="utf-8")
    PUB.write_text(text, encoding="utf-8")
    print(f"explained {len(EXPLAINS)} articles")


if __name__ == "__main__":
    main()
