PEG_REFS = {
    0: ["64"],
    1: ["57", "63"],
    2: ["57", "63", "60"],
    3: ["64", "63"],
    5: ["60", "63"],
}


def main():
    root = Path(__file__).resolve().parents[1]
    src = root / "docs" / "data" / "trademark-palace.json"
    data = json.loads(src.read_text(encoding="utf-8"))
    data["articles"] = ARTICLES
    data["schemaVersion"] = 2

    for floor in data["floors"]:
        for room in floor["rooms"]:
            primary, related = ROOM_REFS.get(room["id"], ([], []))
            room["articleRefs"] = primary
            # related excludes primary
            room["relatedArticleRefs"] = [x for x in related if x not in primary]

    for peg in data["numberPegs"]:
        peg["articleRefs"] = PEG_REFS.get(peg["n"], [])

    src.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    pub = root / "trademark-palace-web" / "public" / "data" / "trademark-palace.json"
    pub.write_text(src.read_text(encoding="utf-8"), encoding="utf-8")
    print("updated", src, "and", pub)


if __name__ == "__main__":
    main()
